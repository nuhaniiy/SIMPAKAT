package com.nurul.simpakat.home.ui.proker;

public class ProkerModel {
    private String unitKerja;
    private String programKerja;
    private String keterangan;
    private String accountCOA;
    private String jenisPembiayaan;
    private String biaya;
    private String bulanKegiatan;

    public String getUnitKerja() {
        return unitKerja;
    }

    public void setUnitKerja(String unitKerja) {
        this.unitKerja = unitKerja;
    }

    public String getProgramKerja() {
        return programKerja;
    }

    public void setProgramKerja(String programKerja) {
        this.programKerja = programKerja;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getAccountCOA() {
        return accountCOA;
    }

    public void setAccountCOA(String accountCOA) {
        this.accountCOA = accountCOA;
    }

    public String getJenisPembiayaan() {
        return jenisPembiayaan;
    }

    public void setJenisPembiayaan(String jenisPembiayaan) {
        this.jenisPembiayaan = jenisPembiayaan;
    }

    public String getBiaya() {
        return biaya;
    }

    public void setBiaya(String biaya) {
        this.biaya = biaya;
    }

    public String getBulanKegiatan() {
        return bulanKegiatan;
    }

    public void setBulanKegiatan(String bulanKegiatan) {
        this.bulanKegiatan = bulanKegiatan;
    }
}
